public class exo14 {
    /*
     * EXERCICE 4 : Faire le compte à rebour à partir de 10, affiche successivment
     * décompte une fois à 0, afficher bonne année !
     */
    public static void main(String[] args) throws Exception {
        /* Afficher le résultat */
        for (int i = 10; i > 0; i--)
            System.out.println(i);
        System.out.println("Bonne année !");
    }
}
