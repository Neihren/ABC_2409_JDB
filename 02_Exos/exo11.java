public class exo11 {
    // EXERCICE 1 : Créer un algo qui définit la valeur A est supérier ou égale à B
    public static void main(String[] args) throws Exception {
        int a = 10;
        int b = 50;
        /* Afficher le résultat */
        if (a < b)
            System.out.println("A < B");
        else if (a > b)
            System.out.println("A > B");
        else
            System.out.println("A = B");
    }
}
